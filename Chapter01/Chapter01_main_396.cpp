#include <cmath>

int main(int argc, char* argv[])
{
   double x = 1.0, y = 2.0, z;
   z = x/y;        // division
   z = x*y;        // multiplication
   z = sqrt(x);    // square root
   z = exp(y);     // exponential function
   z = pow(x, y);  // x to the power of y
   z = M_PI;       // z stores the value of pi

   return 0;
}
